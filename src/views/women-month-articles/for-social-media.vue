            </p>

            <p class="mt-6 text-gray-700">
              However, the ethos of celebrating “struggles for equality” and
              bridging the gender gap is not extinct in our present moment.
              International Women’s Day, declared by the United Nations as a
              global symbol of gender resistance in 1977, came after a
              long-drawn attempt to register protests of women’s rights for
              equal pay, labour, voting rights, property rights and finally to
              condemn violence against women. The day symbolizes a century-long
              protest culture, beginning in 1908 when 15,000 women marched
              through New York City demanding shorter hours, better pay, and
              voting rights. Soon after, in 1909, the Socialist Party of America
              declared the first National Woman&#39;s Day (NWD) as 28 February
              and the women across the United States continued to celebrate NWD
              on the last Sunday of February until 1913.
            </p>

            <p class="mt-6 text-gray-700">
              Moving on from the historic episodes of protest, what
              International Working Women’s Day alerts us today, is essentially
              to recognize struggles and to embellish the idea of “choice” as
              individuals. Womanhood, as a historically constructed idea, has
              been meandering around gendered ideas of labour and the
              invisibilization of the same. Caring mothers, nurturers and
              homemakers essentially make up a large part of this invisible,
              unpaid labour force. Traditionally, for many women, who hold
              employment outside the home, they spend their everyday lives under
              the double burden of housework and then stepping out. We often
              hear stories of mothers and wives waking up hours before the
              family starts their day, to prepare meals (one of the many chores)
              and then step out for work again. Women continue to have longer
              days, more physical exertion and oftentimes poorer nutrition
              intake than the rest of the household, all of which remain largely
              unaddressed. Invisibilization happens, through masking it in the
              twin trope of “love and sacrifice” that have been key in imagining
              womanhood from the patriarchal lens. The most important quality of
              this struggle is that it cuts across class, caste, and ethnic
              lines, making it a global challenge.
            </p>

            <p class="mt-6 text-gray-700">
              When dominant gendered ideas of housework percolate our cultural
              imaginations, it makes womens’ choices obsolete. Using Women’s Day
              as a market strategy to promote celebration through discounts and
              gift vouchers is a prevalent norm in recent years. This only goes
              on to deepen the wedge of invisibilizing the real everyday
              struggles that once our sisters and mothers brought to the
              conversation through historic protests a century ago. The urgent
              need of the hour, then, is to ponder and reconfigure our ideas of
              labour and choices, and create a space that allows women (and
              other marginalized gendered voices) to access, practice and
              register their desires and choices as individuals. Let us, then,
              take up the call to #AcceralateAction (the official campaign for
              International Women’s Day 2025) towards this, amongst other
              ongoing struggles for equality.
