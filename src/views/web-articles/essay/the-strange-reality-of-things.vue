            <p class="mt-6 text-gray-700">
              Prof. Bill Kelly said to me in January 2009, “It’s all your fault.
              All this snow storm and heavy snow, quite unlike a New Haven
              winter - all your fault.” He smiled cheekily. Snow has the
              capacity to breathe into Grief and magnify it like a hot air
              balloon. It passes into the membranes of a landscape and turns it
              all into a psychogeography of Grief. The Parc la Fontaine in
              Montreal, where I walk in the snow during the month of February,
              becomes a site for snow-pleasures. Walking, sledding, various
              forms of snow-sports. Not being sporty, I stare at the huge frozen
              lake, ensconced in white. It seems like it is resting. Processing
              Grief much like Ernaux sitting on a park bench. It doesn’t have to
              perform its pretty-lakeness at this time. It hides; it is in
              hibernation. Giant squirrels come out of the snow and mill out
              about in the harsh winter sun and dive back in. Trees heave in
              their leaflessness. It appears as if they too are renewing their
              passports for the next round of bloom.
            </p>
          </div>

          <div class="mt-4">
            <p class="mt-8 text-gray-700 font-bold text-lg italic">
              Un/situated
            </p>

            <p class="mt-8 text-gray-700">
              The editor who commissioned this piece asked me to write in Bangla
              in keeping with the bilingual tone of the magazine. I was excited
              at the opportunity and then quickly pondered upon my lost
              confidence in writing in my mother tongue. English is now the
              language of my dreams, a language that forces loss into the genre
              of pathos. It doesn’t know how to hold loss in staring it at it
              closely like a microbe in the lab. I write elsewhere about the
              stretching of the English language in holding other the
              intensities of other languages, the comfortable example for me
              being that of Bhakti poetry. Braj, Sanskrit, Telugu, Kashmiri,
              Kannada, Bangla – pour their love and loss collected over two
              thousand years, into the contours of twentieth century English.
              And out of that stretching and molding exercise, like the hands of
              an agile sculptor, appears a peculiarly Indian register of the
              English language. That’s really my craft-tool. Arun Kolatkar’s
              <span class="font-semibold italic">Chaitanya</span> (“he popped a
              stone in his mouth and spat out gods”: there are two poems called
              Chaitanya in the collection
              <span class="font-semibold italic">Jejuri</span>) breathes life
              into this new clay that is the English language paying attention
              to Other emotions. Where loss is loss, you stare at it deeply and
              without fear. Like Surdas telling the story of a dust-laden Radha
              walking the alleyways of Mathura, a madwoman. No holes barred
              there. It is sweeter to listen to in the Brajbhasha in the sung
              forms. But I often listen to quietness and starkness of these
              Englishes, and wonder if they are refuge-homes for an exile like
              Agha Shahid Ali. Shahid writes
              <span class="font-semibold italic">(Snow on the Desert)</span> of
              a geography of loss:
            </p>
            <p class="mt-6 ml-8 italic text-gray-700">
              We are driving slowly, the road is glass.
            </p>
            <p class="mt-1 ml-8 italic text-gray-700">
              “Imagine where we are was a sea once.
            </p>

            <p class="mt-6 ml-8 italic text-gray-700">
              Just imagine!” The sky is relentlessly
            </p>
            <p class="mt-1 ml-8 italic text-gray-700">
              sapphire, and the past is happening quickly:
            </p>
            <p class="mt-6 ml-8 italic text-gray-700">
              the saguaros have opened themselves, stretched
            </p>
            <p class="mt-1 ml-8 italic text-gray-700">
              out their arms to rays millions of years old,
            </p>
            <p class="mt-6 ml-8 italic text-gray-700">
              in each ray a secret of the planet’s
            </p>
            <p class="mt-1 ml-8 italic text-gray-700">
              origin, the rays hurting each cactus
            </p>
            <p class="mt-6 ml-8 italic text-gray-700">
              into memory, a human memory
            </p>
            <p class="mt-1 ml-8 italic text-gray-700">
              for they are human, the Papagos say:
            </p>
            <p class="mt-6 ml-8 italic text-gray-700">
              not only because they have arms and veins
            </p>
            <p class="mt-1 ml-8 italic text-gray-700">
              and secrets. But because they too are a tribe,
            </p>
            <p class="mt-6 ml-8 italic text-gray-700">
              vulnerable to massacre. “It is like
            </p>
            <p class="mt-1 ml-8 italic text-gray-700">
              the end, perhaps the beginning of the world,”
            </p>
            <p class="mt-6 ml-8 italic text-gray-700">
              Sameetah says, staring at their snow-sleeved
            </p>
            <p class="mt-1 ml-8 italic text-gray-700">arms.</p>

            <p class="mt-8 text-gray-700">
              Perhaps, it is from Shahid that we must learn to make a cognitive
              and emotional home inside a strange tongue, a strange landscape,
              strangers who on occasion become friends. Language is the last
              home, the last place to deposit our ongoing ledger-book of loss.
              Shahid knew it well; he treated Tucson and Chile and Kashmir as
              effective props to un/situate our collective, peculiar, modern
              Grief.
            </p>
