          <div class="mt-8">
            <p class="mt-8 text-gray-700 font-bold text-lg italic">
              Strange Unrealities
            </p>

            <p class="mt-8 text-gray-700">
              Joan Didion watches her beloved die in the hospital in the opening
              pages of
              <span class="font-semibold italic"
                >The Year of Magical Thinking</span
              >. She wonders what tenor of reaction would count as Grief.
            </p>

            <p class="mt-6 text-gray-700">
              Routine pleasure-seeking is a kind of Grief. I fiddle around in
              the afternoons on Instagram shopping pages in between reading and
              writing about Didion and Ernaux. I can hear them laughing with/at
              me in empathy. I can hear myself expressing Grief of an unknown
              origin. This, a colleague reminds me, per Freud, is called
              Melancholia – sadness of an unknown origin.
            </p>

            <p class="mt-6 text-gray-700">
              Nothing counts, nothing matters really: we are all in a kind of
              casual nihilism. Except the episodic pleasure of buying expensive
              accessories, that I can forget about in a year. I inherited a
              whole ensemble of white/beige/off-white saris (some of them rare
              gems) from my late grandmother’s closet. She took pleasure in such
              episodes of acquisition too, especially of saris and jewellery. It
              is her routine pleasure-seeking attitude that I seem to have
              inherited. But her empire of things is neatly organized and
              remembered. She seemed to love her saris acutely and individually.
              I have begun to loot that empire slowly. This loot is a source of
              pleasure. I walk around wearing a dead person’s clothing items; I
              get widely complimented. Ernaux, “the girl of ‘58”, aged eighteen
              expressed grief by singing bawdy slur songs with male friends
              flung around her shoulders, after having had a troubling sexual
              escapade that she describes in
              <span class="font-semibold italic">A Girl’s Story</span>, that
              left her feeling violated and pleasured and wanting at the same
              time. This too, is possible. It takes her a book to stomach that
              feeling. That is Didion’s tone too. While watching the floor being
              mopped clean of blood that spilt from her partner’s body, she
              tells herself something to the tune of: this must be grief. Or,
              this too is possible in one’s lifetime. She remembers to renew her
              passport, even as the world shatters around her. She resists the
              obituary request by NYT. It would mean allowing other people into
              the death of her beloved. If it were made public and no more
              private, it would mean that he couldn’t come back.
            </p>

            <p class="mt-6 text-gray-700">
              Annie Ernaux writes at the end of
              <span class="font-semibold italic">Girl’s Story</span>:
            </p>
            <p class="mt-6 ml-8 italic text-gray-700">
              The memory of what I have written is already fading. I do not know
              what this piece of writing is. Even the thing I was pursuing by
              writing this book has dissolved. Among my papers I found a sort of
              note of intent.
            </p>

            <p class="mt-2 ml-6 italic text-gray-700">
              Explore the gulf between the stupefying reality of things that
              happen, at the moment they happen, and years later, the strange
              unreality in which the things that happened are enveloped.
            </p>

            <p class="mt-6 text-gray-700">
              The <span class="font-semibold italic">strange unreality</span> of
              things that actually happen. Like the dead man who lay on the
              crossroad near my place in Vrindavan ( a sacred city of Krishna
              worship in northern India) the other day. Flies whirred about, as
              this stiffened body was lifted by three men onto a van. A few
              policemen watched. Watching war and other people’s backyards being
              blown up by drones and other modern toys on the internet,
              accumulates in our grief pockets only to be poured out into
              shopping websites. Bach’s Concerto in Two Violins helps, Nikhil
              Banerjee’s Raag Megh helps. What helps the most is the daily hunt
              for pretty things on the internet, crudely called shopping. This,
              mind you, is Grief.
            </p>
          </div>

          <div class="mt-4">
            <p class="mt-8 text-gray-700 font-bold text-lg italic">
              A Simple Passion
            </p>

            <p class="mt-8 text-gray-700">
              The <span class="font-semibold italic">strange unreality</span> of
              things that happen in intimate and distant worlds is made
              palatable by making tea, breakfast, laundry, passport renewals,
              and shopping. This is Grief when no one is looking. Dr. Z says, I
              work too much to avoid feeling disenchantment. He used a different
              word I think, but I like to paraphrase him as saying
              ‘disenchantment’. Internet outrage about war and other things, I
              believe, is fundamentally a response of disbelief at the
              <span class="font-semibold italic">Unreality of Things.</span>
              It seems unreal that I am drinking coffee in my sixth floor
              Vrindavan studio, while another person’s whole world is getting
              bombed or earthquaked or hurricaned. Only events and eventfulness
              strike us as response-worthy. We are not expressing outrage on
              social media about the skewed sex ratio of 700 in Haryana, or
              about widespread land grab for ecotourism in forested landscapes.
              Things which do not take episodic form do not provoke the
              pleasures of our kneejerk outrage. Outrage, anger and their
              immediate kneejerks are Grief.
            </p>

            <p class="mt-6 text-gray-700">
              In <span class="font-semibold italic">A Simple Passion</span>,
              Annie Ernaux watches herself (in writing, at a later point)
              sitting by the telephone waiting for the arrival of her rather
              unavailable lover. For Ernaux, this is a relationship that is
              structured around her own loneliness rather than in the company of
              another person. Much of it plays out by the telephone. She takes
              trips by herself, walks in the public spaces, sits on benches –
              almost as a madwoman nursing what she calls “a simple passion”. In
              the end of the book, Ernaux writes that it is a luxury “to be able
              to live out a passion for a man or a woman.” Most of us are denied
              this opportunity for passion to live itself out like a resplendent
              California forest fire. Most of us don’t give in to the macabre
              pleasure of watching buildings tumble into ashen debris in
              Palestine videos. Most of us, when presented with the opportunity
              to let passion enter our narratives, act squeamishly, and in the
              end, most likely, will be passing up that opportunity. We can’t
              afford this luxury. But we can afford Grief - the less attractive
              cousin of Passion. We can buy things and express outrage on the
              internet instead of facing another person sitting on a park bench
              on a hot afternoon. These are short forms for the long form of
              Ernaux – taking a book to stomach that feeling. These short forms
              dig a mammoth hole in our hearts that we then fill with routine
              pleasures of late capitalist grief. Buying things on Instagram and
              raising one’s shrillness about today’s or yesterday’s theatre of
              unspeakable trauma are, I’d say, pretty much the same thing. Both
              are expressions of the denied luxury – passion; both are
              expressions of the cheaper and yet equally damaging sentiment we
              do have plenty of – Grief.
            </p>
          </div>

          <div class="mt-4">
            <p class="mt-8 text-gray-700 font-bold text-lg italic">Snow</p>

            <p class="mt-8 text-gray-700">
              Anne Carson wrote a perfect essay on snow in the LARB. I learnt to
              appreciate the pathos of snow from that essay. I also pondered
              snow while watching Pawel Pawlikowski’s 2018 film
              <span class="font-semibold italic">Cold War</span>, in a snowed
              Montreal in 2022. Stretches of my life have been spent amid snow.
              I have a difficult relationship with it; it coughs up all my pain.
              The sheer bareness of it. I never think of Shammi Kapoor and
              Sharmila Tagore in Kashmir or Shah Rukh and Juhi in
              chiffon-wrapped Switzerland, making warm love amid cold environs.
              I think of death and renewal. Like the dead leaves of autumn that
              denote an early message of cycles of renewal. Rebirth.
            </p>

            <p class="mt-6 text-gray-700">
              In the year 2009, I came back from India in January to begin the
              spring term. I arrived into the Newark airport to find the world
              had turned to snow. I lugged my big suitcase across air-trains and
              platforms to make it to the Amtrak connection to New Haven. My
              Russian-American friend from graduate school, M, came to pick me
              up in her car at the New Haven Union station. My complete
              alienation in the United States of America was briefly kept in
              abeyance by second world solidarities. I have spent any evenings
              in the early years of graduate school (where I used not to eat for
              days on end) in MS’s home, eating hot, nourishing borscht. I
              remember particularly the Russian dumplings lined with vodka. M
              towered over me, a lithe Caucasian woman of almost of six feet in
              height. I remained ever anxious about getting assignments done on
              time, and M mocked me playfully. She had been an investment
              banker, having studied comparative literature and mathematics at
              the undergrad level. This amazed me. I longed for the neatness of
              differential calculus, sitting in New Haven, at the Bass Library
              kiosks, wondering about messy texts of Gramsci and Althusser.
            </p>
