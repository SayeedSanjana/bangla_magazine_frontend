          <div class="mt-8">
            <p class="mt-4 text-gray-700">
              February 21st 2025 marks the 25th anniversary of International
              Mother Language Day that was first proclaimed by UNESCO and later
              adopted by the UN. While the history of this day is conditioned by
              the liberation movement in Bangladesh and its attendant violence,
              it has now become an occasion for both commemoration and
              celebration. Significant part of the Bengali-speaking population
              across the world remembers the martyrs of Bangla who laid down
              their lives for claiming the right to speak their mother tongue in
              1952. Their martyrdom would eventually galvanize the masses in
              East Pakistan and culminate into the formation of Bangladesh in
              1971. This is one of the rare examples in history where a
              linguistic movement transformed into a national liberation
              movement. Such sombre legacy of February 21st coexists with the
              global recognition of this day as a reminder of the significance
              of multiple languages and coexistence of many linguistic cultures
              across planet earth. In our present moment, the need for
              communication in a common global language especially in
              professional spaces acts as a threat to multilingualism.
              Furthermore, vernacular and indigenous languages as well as people
              speaking them are constantly being pushed towards the margin by
              nation- states prioritizing a single language or selecting a group
              of languages as representative of all people living within its
              borders. Under such circumstances, International Mother Language
              Day is a crucial reminder of the necessity to make conscious
              collective efforts for the preservation of multilingualism and
              polyvocality on a world scale. BANGLA is one among many such
              endeavours to situate and celebrate plurality of languages and
              cultures.
            </p>

            <p class="mt-4 text-gray-700">
              BANGLA intends to extend a worldview of linguistic freedom and
              exchange that initiate language movements like the one marked by
              February 21st. Instead of treating the day as a historical
              metaphor limited to a particular geopolitical reality, we would
              like to remember that the Bangla language movement is one among
              many such language movements in the world that have a common
              shared objective. For example, in South Asia, the longue dur√©e of
              language movements is marked by the Urdu movement in the northern
              and central regions of modern day India in the 1850s; Swabhasha
              movement of Sri Lanka in the 1930s; Anti-Hindi movement in the
              southern regions of modern day India in 1937; Vishalandhara
              movement in 1953 culminating into Telangana movement of 2014; Mei
              Tei movement, Assamese movement and Nepali movement through the
              20th century. On the international front, some of the notable
              language movements include the Afrikaans language movement in
              South Africa, Gaelic Revival in Ireland, and Pan-Scandinavianism
              in the 19th century; the Pan- South-Slavic Ilyrian movement in the
              first half and the Baloch movement in the second half of the 20th
              century; Quebecois ‚ÄòQuiet Revolution‚Äô in the 1960s, and Andalusian
              movement in the 2000s. Each of these movements have their own
              politico-historical specificities of emergence but they are also
              bound by the common shared objective of people coming together to
              safeguard their own language and culture. Such a mapping of
              language movements underlines the overlapping global struggle for
              linguistic representation across generations and geographical
              locales. Furthermore, they necessitate conversations regarding the
              relational nature of crisis that leads to such movements in the
              first place. Drawing on such observations, BANGLA locates February
              21st in the longue dur√©e of struggle for linguistic expression,
              language conservation, and collective cultural assertion.
            </p>
            <p class="mt-4 text-gray-700">
              BANGLA provides a platform for conversation that addresses the
              history of language movements, intervenes in contemporary debates
              of minoritization as well as marginalization of languages, and
              engages with their futurities through fiction, non-fiction,
              poetry, performance and artworks. BANGLA has recently begun its
              journey as a Canada-based non- profit network by Bangla speakers
              and people who have come in contact with Bangla language or
              culture through their lived experiences. However, we aim to expand
              into a platform for generative interactions between people
              belonging to different linguistic cultures in their own mother
              languages, adopted languages, and through translations of the
              same. Thus, the historical legacy of language movements is
              embedded and extended in the very conceptualization of BANGLA as
              an intersectional multilingual platform. Fittingly then, our first
              in-person event will be a celebration of the 25th International
              Mother Language Day at Concordia University in Montreal.
