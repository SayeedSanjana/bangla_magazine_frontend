              urged the General Secretary of the United Nations to consider 21
              February as a global occasion, focusing on saving endangered
              languages. With the support of almost all member countries, this
              proposal got approved by the United Nations Educational,
              Scientific and Cultural Organization in 1999, and was formally
              adopted by the United Nations General Assembly in 2002.
            </p>
            <p class="mt-4 text-gray-700">
              However, in the current era of extensive global connectivity, it
