              wipe out South Asian languages. Both Muslims and non-Muslims
              learnt Persian as it provided incentives to secure jobs. A few
              inscriptions from western India, Z.A. Desai notes, are bilingual,
              written in Persian or Arabic and in Sanskrit or local dialect(s).
              This long coexistence with Persian remarkably enriched the Bengali
              vocabulary.
