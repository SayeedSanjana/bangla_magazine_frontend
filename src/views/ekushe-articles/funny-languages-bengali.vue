            <h2 class="text-2xl font-bold mt-8">Glocal Languages</h2>

            <p class="mt-4 text-gray-700">
              Bangla or Bengali is the sixth most-spoken language in the world
              in terms of native speakers, with an estimated number of about 275
              million speakers (other estimates: 250 million). Most of them
              reside in the South Asian nations of India and Bangladesh, while a
              sizable number moved to Europe, North America, and other parts of
              the globe. The term glocal is used in geography to connote
              integration between the local and global, and connectedness
              produced by travel, business, and communications. Bengali, we
              might argue, is now a glocal language: its local identity
              originates in South Asia but the language has a global imprint
              thanks to Bengali-speakers who settled in different parts of the
              world. Not surprisingly, this global presence too grows local
              roots, as examples from London and New York tell us. The
              neighbourhood of Brick Lane, Tower Hamlets, and East End by the
