            <h2 class="text-2xl font-bold mt-8">Where are we headed?</h2>
            <p class="mt-4 text-gray-700">
              Languages, therefore, are ever-evolving. Native speakers of
              certain languages (e.g. Uzbek) can take to new vernaculars, while
              native speakers of others (e.g. Bengali) adopt non-native words to
              a point that they comfortably integrate with everyday speech. Let
              us now approach these phenomena from a different perspective: one
              where native speakers of languages find themselves in alien
              environments, are absorbed in a new linguistic set-up, and still
              leave traces of their origins. To substantiate this, I will insert
              two examples related to the Dutch East India Company (the VOC), a
              European mercantile and colonial power, that traded in the Indian
              Ocean between 1600 and 1800 CE in diverse commodities, primarily
              spices and textiles. South Asia and Southeast Asia were two major
              regions of their commercial operations.
            </p>
            <p class="mt-4 text-gray-700">
              Early on in the 1620s the VOC top brass stressed the company‚Äôs
              need for slaves for their colonies in Southeast Asia, as labour
              was scarce. During 1622-1623, Jan Pieterszoon Coen, the VOC
              governor-general in Batavia, modern-day Jakarta, recommended
              procuring large number of slaves from South Asia, particularly the
              Coromandel Coast in southern India, Bengal, and Sri Lanka, to
              establish the Dutch colonies in Southeast Asia. Such enslaved
              individuals who made their way to Southeast Asia are faceless ‚Äì
              yet traces of their identities and afterlives are discernible.
              These slaves were invariably converted to Christianity and
              rechristened. Their new names pointed to their provenance, e.g., a
              Susana van Bengalen (Susana from Bengal) appears in the archives
              of the VOC in 1753 CE. Instruction in main ideas of Christianity
              for these newly-converted Christians could be imparted in
              Portuguese, or Malayalam, Tamil, and Bengali ‚Äì as Manilata
              Choudhuri shows in her essay ‚ÄúThe Mardijkers of Batavia:
              Construction of a colonial identity (1619-1650).‚Äô‚Äô So, native
              languages could find some space, although ever so slight, in an
              environment where they barely exerted any power.
            </p>
            <p class="mt-4 text-gray-700">
