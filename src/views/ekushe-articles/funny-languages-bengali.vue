            <h2 class="text-2xl font-bold mt-8">
              Bengali and its hunger for nouns
            </h2>

            <p class="mt-8 text-gray-700">
              Peggy Mohan, in
              <span class="italic"
                >Wanderers, Kings, Merchants: The Story of India</span
              >
              through its Languages, makes a strong case for language mixture
              and migration in South Asia, with examples from Sanskrit,
              Malayalam, Persian, and Nagamese. Elite languages like Sanskrit
              and Persian, Mohan argues, entered South languages where they made
              their own space but did not change their host‚Äôs ‚Äì if I might use
              that word ‚Äì grammar and verbs. In this respect, Mohan proffers
              evidence from Sanskrit-Malayalam and Persian-Hindi/Urdu
              relationships. Malayalam, a language from Kerala in southwestern
              India, is part of the Dravidian language family but its top layer
              is replete with Sanskrit words, especially nouns. The entry of
              Sanskrit in Malayalam dates back to 8th century CE when local
              rulers invited Namboodiri brahmins (priests) from western India to
              officiate fire rituals based on the Vedas (scriptures comprising
              poems or hymns in archaic Sanskrit on philosophy, gods, and
              rituals, put into writing from mid-second millennium BCE; created
              by successive groups of Indo-Aryan speakers of the Indo-Iranian
              branch of the Indo-European family who moved to South Asia from
              about c. 3000 BCE). Much as the Namboodiri brahmins of Kerala
              ensured Sanskrit ‚Äì and their pronunciation of it ‚Äì remained
              pristine, they eventually adopted Malayalam as the vernacular, and
              finally added Sanskrit words to written Malayalam. A familiar
              process unfolded with Central Asians arriving in South Asia from
              the 13th century CE. They nurtured Persian as an elite language
              but switched from languages like Uzbek to early Hindi and later
              Urdu as their vernacular; however, Hindi or Urdu grammar was not
              altered along Persian model(s). Similar to Malayalam,
              <span class="italic">ghazals</span>
              (poems) composed in Urdu from the 1700s by individuals of Central
